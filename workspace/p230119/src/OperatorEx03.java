
public class OperatorEx03 {

	public static void main(String[] args) {

		
		// 증가, 감소 연산자 (1증가, 1감소)
		// ++ 1증가
		// -- 1감소
		
		int num = 5;
		System.out.println("num >> " + num);
		
		
		// num = num + 1; @ (num++;) 띄어 쓰기 없이 붙여서 사용 하라
		// num = num + 1; 1증가 식를 자주 사용 하게 됌 
		// num++;				// 1증가 연산자를 쓰게 됌
		// num = num +(2,3,4,5) 증가폭이 넓을 경우
		
		num++;
		System.out.println("num >> " + num);
		
		
		
	}

}
