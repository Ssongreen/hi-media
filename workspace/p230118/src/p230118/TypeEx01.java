package p230118;

public class TypeEx01 {
// main method
		public static void main(String[] args) {
			
			// 변수 선언과 동시에 초기화
			short one = 300;
			System.out.println("one : " + one);
			
			long two = 100000000000L;
			System.out.println("two  : " + two);
			
			byte three = -5;
			System.out.println("three : " + three);
			
			
			
		}
		
}
